/*
 * *****
 * *****
 * ***** 를 반복해서 찍고 싶다.
 * 
 */
public class Loopmain02 {

	public static void main(String[] args) {
		
		for(int i =1; i<=3; ++i) {
			
			for(int j = 1; j<=5; j++) { //이문장을 3번 반복하겠다는 것
		
				System.out.print("*");
			}System.out.println();
		}
	}
}
 
	
